/// Human-readable name of a memory-mapped register for `--log-mmio` / `--watch`
/// output. Names transcribed from references/mmio.md (§6-9). Returns `None` for
/// addresses with no documented register (open bus / plain memory).
pub fn mmio_reg_name(addr: u32) -> Option<&'static str> {
    let off = (addr & 0xFFFF) as u16;
    let name = match off {
        0x2100 => "INIDISP",
        0x2101 => "OBSEL",
        0x2102 => "OAMADDL",
        0x2103 => "OAMADDH",
        0x2104 => "OAMDATA",
        0x2105 => "BGMODE",
        0x2106 => "MOSAIC",
        0x2107 => "BG1SC",
        0x2108 => "BG2SC",
        0x2109 => "BG3SC",
        0x210A => "BG4SC",
        0x210B => "BG12NBA",
        0x210C => "BG34NBA",
        0x210D => "BG1HOFS",
        0x210E => "BG1VOFS",
        0x210F => "BG2HOFS",
        0x2110 => "BG2VOFS",
        0x2111 => "BG3HOFS",
        0x2112 => "BG3VOFS",
        0x2113 => "BG4HOFS",
        0x2114 => "BG4VOFS",
        0x2115 => "VMAIN",
        0x2116 => "VMADDL",
        0x2117 => "VMADDH",
        0x2118 => "VMDATAL",
        0x2119 => "VMDATAH",
        0x211A => "M7SEL",
        0x211B => "M7A",
        0x211C => "M7B",
        0x211D => "M7C",
        0x211E => "M7D",
        0x211F => "M7X",
        0x2120 => "M7Y",
        0x2121 => "CGADD",
        0x2122 => "CGDATA",
        0x2123 => "W12SEL",
        0x2124 => "W34SEL",
        0x2125 => "WOBJSEL",
        0x2126 => "WH0",
        0x2127 => "WH1",
        0x2128 => "WH2",
        0x2129 => "WH3",
        0x212A => "WBGLOG",
        0x212B => "WOBJLOG",
        0x212C => "TM",
        0x212D => "TS",
        0x212E => "TMW",
        0x212F => "TSW",
        0x2130 => "CGWSEL",
        0x2131 => "CGADSUB",
        0x2132 => "COLDATA",
        0x2133 => "SETINI",
        0x2134 => "MPYL",
        0x2135 => "MPYM",
        0x2136 => "MPYH",
        0x2137 => "SLHV",
        0x2138 => "OAMDATAREAD",
        0x2139 => "VMDATALREAD",
        0x213A => "VMDATAHREAD",
        0x213B => "CGDATAREAD",
        0x213C => "OPHCT",
        0x213D => "OPVCT",
        0x213E => "STAT77",
        0x213F => "STAT78",
        0x2140 => "APUIO0",
        0x2141 => "APUIO1",
        0x2142 => "APUIO2",
        0x2143 => "APUIO3",
        0x2180 => "WMDATA",
        0x2181 => "WMADDL",
        0x2182 => "WMADDM",
        0x2183 => "WMADDH",
        0x4200 => "NMITIMEN",
        0x4201 => "WRIO",
        0x4202 => "WRMPYA",
        0x4203 => "WRMPYB",
        0x4204 => "WRDIVL",
        0x4205 => "WRDIVH",
        0x4206 => "WRDIVB",
        0x4207 => "HTIMEL",
        0x4208 => "HTIMEH",
        0x4209 => "VTIMEL",
        0x420A => "VTIMEH",
        0x420B => "MDMAEN",
        0x420C => "HDMAEN",
        0x420D => "MEMSEL",
        0x4210 => "RDNMI",
        0x4211 => "TIMEUP",
        0x4212 => "HVBJOY",
        0x4213 => "RDIO",
        0x4214 => "RDDIVL",
        0x4215 => "RDDIVH",
        0x4216 => "RDMPYL",
        0x4217 => "RDMPYH",
        0x4218 => "JOY1L",
        0x4219 => "JOY1H",
        0x421A => "JOY2L",
        0x421B => "JOY2H",
        0x421C => "JOY3L",
        0x421D => "JOY3H",
        0x421E => "JOY4L",
        0x421F => "JOY4H",
        0x4300..=0x437F => {
            return dma_reg_name(off);
        }
        _ => return None,
    };
    Some(name)
}

/// DMA channel register name for $43x0-$43xB (x = channel 0-7), per mmio.md §9.
fn dma_reg_name(off: u16) -> Option<&'static str> {
    match off & 0x000F {
        0x0 => Some("DMAP"),
        0x1 => Some("BBAD"),
        0x2 => Some("A1TL"),
        0x3 => Some("A1TH"),
        0x4 => Some("A1B"),
        0x5 => Some("DASL"),
        0x6 => Some("DASH"),
        0x7 => Some("DASB"),
        0x8 => Some("A2AL"),
        0x9 => Some("A2AH"),
        0xA => Some("NLTR"),
        0xB | 0xF => Some("UNUSED"),
        _ => None,
    }
}
